#!/usr/bin/env node
// build.js — dependency-free bundler. Concatenates workflows/src/*.js into the
// single self-contained workflows/pipeline.js. The bundle MUST begin with
// `export const meta` + `export const PIPELINE_VERSION` (hoisted from
// pipeline_entry.js) so the runtime reads them at parse; run() and helpers read
// those consts only at call time, so hoisting is safe. All import lines are
// dropped; `export X` -> `X` for every non-public declaration; `export default`
// (the run entry) is kept verbatim.
import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const HERE = dirname(fileURLToPath(import.meta.url));
const SRC = join(HERE, 'src');
const OUT = join(HERE, 'pipeline.js');

// Pinned concat order. dedupCrossAgent (filterFindings) must precede applyChallenges.
// pipeline_entry.js is emitted LAST (its run() references everything above).
const ORDER = [
  'findingDedup.js', 'filterFindings.js', 'mergeFindings.js',
  'applyValidations.js', 'applyChallenges.js', 'registry.js', 'args.js',
  'stages.js', 'pipeline_entry.js',
];

const HOIST = /^\s*export\s+const\s+(meta|PIPELINE_VERSION)\b/;

function present() {
  const found = new Set(readdirSync(SRC).filter((f) => f.endsWith('.js')));
  return ORDER.filter((f) => found.has(f));
}

// Drop import lines and the hoisted consts (emitted at the top instead); rewrite
// `export X` -> `X`, but keep `export default` (the run entry) verbatim.
function strip(source) {
  const out = [];
  for (const line of source.split('\n')) {
    if (/^\s*import\s.+from\s.+;?\s*$/.test(line)) continue;
    if (HOIST.test(line)) continue;
    if (/^\s*export\s+default\b/.test(line)) { out.push(line); continue; }
    out.push(line.replace(/^(\s*)export\s+(async function|function|const|let|class|{)/, '$1$2'));
  }
  return out.join('\n');
}

function build() {
  const files = present();
  // 1) Hoist the public surface so the bundle's first line is `export const meta`.
  const hoisted = [];
  for (const file of files) {
    for (const line of readFileSync(join(SRC, file), 'utf8').split('\n')) {
      if (HOIST.test(line)) hoisted.push(line);
    }
  }
  const parts = [...hoisted, '// GENERATED by workflows/build.js — do not edit by hand.'];
  // 2) Emit every module body (public consts already hoisted, imports dropped).
  for (const file of files) {
    parts.push(`// --- ${file} ---`);
    parts.push(strip(readFileSync(join(SRC, file), 'utf8')));
  }
  return parts.join('\n') + '\n';
}

writeFileSync(OUT, build());
console.log(`built ${OUT}`);
